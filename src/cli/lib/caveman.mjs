const RULE_FUNCTIONS = {
  splitSentences,
  resolvePronouns,
  activeVoice,
  presentTense,
  lemmatizeNouns,
  applyAbbreviations,
  arrowCausality,
  removeIntensifiers,
  removeArticles,
  removeConnectives,
  limitWords,
  limitWordsUltra,
  checkCompleteness,
};

/**
 * Compress text using Caveman semantic compression.
 *
 * @param {string} text - The text to compress
 * @param {'light'|'semantic'|'aggressive'} [level='semantic'] - Compression level
 * @returns {{ text: string, originalTokens: number, compressedTokens: number, tokenSavings: number, reductionPercent: string, level: string }}
 */
export function compress(text, level = 'semantic') {
  if (!text || text.trim().length === 0) {
    return {
      text: '',
      originalTokens: 0,
      compressedTokens: 0,
      tokenSavings: 0,
      reductionPercent: '0%',
      level,
    };
  }

  const originalTokens = estimateTokens(text);
  const config = COMPRESS_LEVELS[level] || COMPRESS_LEVELS.semantic;
  const rules = config.rules;

  let result = text;

  // Step 1: Split into sentences (always done for semantic/aggressive)
  let sentences = [result];
  if (rules.includes('splitSentences')) {
    sentences = splitSentences(result);
  }

  // Step 2: Apply array-level rules first (resolvePronouns needs full context)
  if (rules.includes('resolvePronouns')) {
    sentences = resolvePronouns(sentences);
  }

  // Step 3: Apply per-sentence rules
  const processed = sentences.map(sentence => {
    let s = sentence;

    for (const ruleName of rules) {
      if (ruleName === 'splitSentences') continue; // Already done
      if (ruleName === 'resolvePronouns') continue; // Already done at array level
      if (ruleName === 'checkCompleteness') continue; // Done at end

      const ruleFn = RULE_FUNCTIONS[ruleName];
      if (ruleFn) {
        s = ruleFn(s);
      }
    }

    // Final completeness check
    if (rules.includes('checkCompleteness')) {
      s = checkCompleteness(s);
    }

    return s;
  }).filter(s => s !== null && s.trim().length > 0);

  // Step 3: Rejoin sentences
  result = processed.join('. ') + (processed.length > 0 ? '.' : '');

  // Clean up: normalize spacing
  result = result
    .replace(/\s{2,}/g, ' ')
    .replace(/\s+\./g, '.')
    .replace(/\.{2,}/g, '.')
    .replace(/,\s*,/g, ',')
    .trim();

  const compressedTokens = estimateTokens(result);
  const tokenSavings = originalTokens - compressedTokens;
  const reductionPercent = originalTokens > 0
    ? Math.round((tokenSavings / originalTokens) * 100)
    : 0;

  return {
    text: result,
    originalTokens,
    compressedTokens,
    tokenSavings,
    reductionPercent: `${reductionPercent}%`,
    level,
  };
}

/**
 * Estimate token count using word-boundary regex.
 * Rough approximation: ~1.3 tokens per word for English text.
 *
 * @param {string} text
 * @returns {number}
 */
export function estimateTokens(text) {
  if (!text) return 0;
  // Count words (split on whitespace and punctuation boundaries)
  const words = text.split(/[\s]+/).filter(w => w.length > 0);
  // Rough estimate: 1.3 tokens per word (GPT tokenizer average)
  return Math.ceil(words.length * 1.3);
}

/**
 * Get available compression levels and their descriptions.
 * @returns {object}
 */
export function getLevels() {
  return Object.entries(COMPRESS_LEVELS).reduce((acc, [key, val]) => {
    acc[key] = { description: val.description, expectedSavings: val.expectedSavings };
    return acc;
  }, {});
}

