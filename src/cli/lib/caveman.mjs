// CLI entry point
// ---------------------------------------------------------------------------

function parseArgs() {
  const args = process.argv.slice(2);
  const opts = {
    text: '',
    file: '',
    level: 'semantic',
    format: 'text',
    stats: false,
  };

  for (let i = 0; i < args.length; i++) {
    switch (args[i]) {
      case '--text':
        opts.text = args[++i];
        break;
      case '--file':
        opts.file = args[++i];
        break;
      case '--level':
        opts.level = args[++i];
        if (!COMPRESS_LEVELS[opts.level]) {
          console.error(`Error: Unknown level "${opts.level}". Use: light, semantic, aggressive, ultra`);
          process.exit(1);
        }
        break;
      case '--format':
        opts.format = args[++i];
        break;
      case '--stats':
        opts.stats = true;
        break;
      case '--levels':
        console.log(JSON.stringify(getLevels(), null, 2));
        process.exit(0);
      case '--help':
      case '-h':
        printHelp();
        process.exit(0);
    }
  }

  return opts;
}

