// ---------------------------------------------------------------------------
// Rule 1: Sentence splitting
// ---------------------------------------------------------------------------

function splitSentences(text) {
  // Split on sentence boundaries but preserve code blocks and URLs
  const codeBlocks = [];
  const urls = [];

  // Protect code blocks
  let protected_ = text.replace(/```[\s\S]*?```/g, (match) => {
    codeBlocks.push(match);
    return `__CODEBLOCK_${codeBlocks.length - 1}__`;
  });

  // Protect URLs
  protected_ = protected_.replace(/https?:\/\/\S+/g, (match) => {
    urls.push(match);
    return `__URL_${urls.length - 1}__`;
  });

  // Split sentences
  const sentences = protected_
    .split(/(?<=[.!?])\s+/)
    .map(s => s.trim())
    .filter(s => s.length > 0);

  // Restore code blocks and URLs
  return sentences.map(s => {
    let restored = s;
    codeBlocks.forEach((block, i) => {
      restored = restored.replace(`__CODEBLOCK_${i}__`, block);
    });
    urls.forEach((url, i) => {
      restored = restored.replace(`__URL_${i}__`, url);
    });
    return restored;
  });
}

// ---------------------------------------------------------------------------
// Rule 2: Pronoun resolution (simplified)
// ---------------------------------------------------------------------------

function resolvePronouns(sentences) {
  // Simple heuristic: replace "it", "they", "them" with last noun phrase
  // when the pronoun is the first word of a sentence
  let lastNoun = '';

  return sentences.map((sentence, idx) => {
    const words = sentence.split(/\s+/);
    if (words.length === 0) return sentence;

    const firstWord = words[0].toLowerCase().replace(/[.,!?;:]$/, '');

    // Track potential noun phrases (capitalized words that aren't sentence starters)
    const nounMatch = sentence.match(/\b([A-Z][a-z]+(?:\s+[A-Z][a-z]+)*)\b/g);
    if (nounMatch && idx > 0) {
      lastNoun = nounMatch[nounMatch.length - 1];
    }

    // Resolve pronouns at sentence start
    if (['it', 'they', 'them', 'this'].includes(firstWord) && lastNoun) {
      const rest = words.slice(1).join(' ');
      return `${lastNoun} ${rest}`;
    }

    return sentence;
  });
}

// ---------------------------------------------------------------------------
// Rule 3: Active voice transformation
// ---------------------------------------------------------------------------

function activeVoice(sentence) {
  let result = sentence;

  // Apply passive → active transformations
  for (const [passive, active] of Object.entries(PASSIVE_TO_ACTIVE)) {
    // Match "X is/was/are/were VERB-ed by Y" pattern
    const regex = new RegExp(
      `(\\S+)\\s+${passive.replace(/\s+/g, '\\s+')}\\s+(\\S+)`,
      'gi'
    );
    result = result.replace(regex, (_, subject, agent) => {
      return `${agent} ${active} ${subject}`;
    });
  }

  return result;
}

// ---------------------------------------------------------------------------
// Rule 4: Present tense normalization
// ---------------------------------------------------------------------------

function presentTense(sentence) {
  const words = sentence.split(/\s+/);
  const normalized = words.map(word => {
    // Strip trailing punctuation for lookup
    const clean = word.replace(/[.,!?;:]+$/, '');
    const punct = word.slice(clean.length);
    const lower = clean.toLowerCase();

    if (PAST_TO_PRESENT[lower]) {
      const present = PAST_TO_PRESENT[lower];
      // Preserve capitalization
      if (clean[0] === clean[0]?.toUpperCase() && clean[0] !== clean[0]?.toLowerCase()) {
        return present[0].toUpperCase() + present.slice(1) + punct;
      }
      return present + punct;
    }

    // Regular verbs: strip -ed suffix
    if (lower.endsWith('ed') && lower.length > 3) {
      const base = clean.slice(0, -2);
      // Handle doubled consonants (stopped → stop)
      if (base.length >= 3 && base[base.length - 1] === base[base.length - 2]) {
        return base.slice(0, -1) + punct;
      }
      // Handle -ied → -y (carried → carry)
      if (base.endsWith('i') && lower.length > 4) {
        return base.slice(0, -1) + 'y' + punct;
      }
      return base + punct;
    }

    return word;
  });

  return normalized.join(' ');
}

// ---------------------------------------------------------------------------
// Rule 5: Intensifier removal
// ---------------------------------------------------------------------------

function removeIntensifiers(sentence) {
  const words = sentence.split(/\s+/);
  const filtered = words.filter(word => {
    const clean = word.toLowerCase().replace(/[.,!?;:]+$/, '');
    return !INTENSIFIERS.has(clean);
  });
  return filtered.join(' ');
}

// ---------------------------------------------------------------------------
// Rule 6: Article removal
// ---------------------------------------------------------------------------

function removeArticles(sentence) {
  const words = sentence.split(/\s+/);
  // Don't remove if it would leave fewer than 3 words
  if (words.length <= 3) return sentence;

  const filtered = words.filter(word => {
    const clean = word.toLowerCase().replace(/[.,!?;:]+$/, '');
    return !ARTICLES.has(clean);
  });

  // Safety: if removal leaves < 3 words, keep original
  if (filtered.length < 3) return sentence;
  return filtered.join(' ');
}

// ---------------------------------------------------------------------------
// Rule 7: Connective elimination
// ---------------------------------------------------------------------------

function removeConnectives(sentence) {
  const lower = sentence.toLowerCase();

  for (const connective of CONNECTIVES) {
    // Match connective as whole phrase (case-insensitive)
    const regex = new RegExp(
      '\\b' + connective.replace(/\s+/g, '\\s+') + '\\b,?\\s*',
      'gi'
    );
    sentence = sentence.replace(regex, '');
  }

  // Clean up double spaces and leading/trailing commas
  return sentence.replace(/\s{2,}/g, ' ').replace(/^,\s*/, '').replace(/,\s*$/, '').trim();
}

// ---------------------------------------------------------------------------
// Rule 8: Word limit per sentence
// ---------------------------------------------------------------------------

function limitWords(sentence, maxWords = 5) {
  const words = sentence.split(/\s+/);
  if (words.length <= maxWords) return sentence;

  // Try to split on comma first
  const commaIdx = sentence.indexOf(',');
  if (commaIdx > 0 && commaIdx < sentence.length / 2) {
    const firstPart = sentence.slice(0, commaIdx).trim();
    const firstWords = firstPart.split(/\s+/);
    if (firstWords.length <= maxWords) {
      return firstPart;
    }
  }

  // Truncate to maxWords
  return words.slice(0, maxWords).join(' ');
}

// ---------------------------------------------------------------------------
// Rule 9: Logical completeness check
// ---------------------------------------------------------------------------

function checkCompleteness(sentence) {
  const words = sentence.split(/\s+/).filter(w => w.length > 0);
  // Reject sentences with fewer than 2 meaningful words
  if (words.length < 2) return null;

  // Reject sentences that are just punctuation/numbers
  const meaningfulWords = words.filter(w => /[a-zA-Z]{2,}/.test(w));
  if (meaningfulWords.length === 0) return null;

  return sentence;
}

// ---------------------------------------------------------------------------
// Rule 10: Tech abbreviation application (ultra mode)
// ---------------------------------------------------------------------------

function applyAbbreviations(sentence) {
  let result = sentence;

  // Apply multi-word abbreviations first (longest match first)
  const multiWord = Object.entries(TECH_ABBREVIATIONS)
    .filter(([k]) => k.includes(' '))
    .sort((a, b) => b[0].length - a[0].length);

  for (const [full, abbr] of multiWord) {
    const regex = new RegExp('\\b' + full.replace(/\s+/g, '\\s+') + '\\b', 'gi');
    result = result.replace(regex, abbr);
  }

  // Apply single-word abbreviations
  const words = result.split(/\s+/);
  const abbreviated = words.map(word => {
    // Strip trailing punctuation for lookup
    const clean = word.replace(/[.,!?;:]+$/, '');
    const punct = word.slice(clean.length);
    const lower = clean.toLowerCase();

    if (TECH_ABBREVIATIONS[lower]) {
      const abbr = TECH_ABBREVIATIONS[lower];
      // Preserve capitalization for acronyms
      if (abbr === abbr.toUpperCase() && abbr.length <= 4) {
        return abbr + punct;
      }
      // Preserve original capitalization
      if (clean[0] === clean[0]?.toUpperCase() && clean[0] !== clean[0]?.toLowerCase()) {
        return abbr[0].toUpperCase() + abbr.slice(1) + punct;
      }
      return abbr + punct;
    }
    return word;
  });

  return abbreviated.join(' ');
}

// ---------------------------------------------------------------------------
// Rule 11: Noun lemmatization (plural→singular, -tion→verb)
// ---------------------------------------------------------------------------

function lemmatizeNouns(sentence) {
  const words = sentence.split(/\s+/);
  const lemmatized = words.map(word => {
    const clean = word.replace(/[.,!?;:]+$/, '');
    const punct = word.slice(clean.length);
    const lower = clean.toLowerCase();

    // Check explicit lemma map
    if (NOUN_LEMMAS[lower]) {
      const lemma = NOUN_LEMMAS[lower];
      if (clean[0] === clean[0]?.toUpperCase() && clean[0] !== clean[0]?.toLowerCase()) {
        return lemma[0].toUpperCase() + lemma.slice(1) + punct;
      }
      return lemma + punct;
    }

    // Rule-based: -s/-es plural → singular
    if (lower.endsWith('ies') && lower.length > 4) {
      return clean.slice(0, -3) + 'y' + punct;
    }
    if (lower.endsWith('ves') && lower.length > 4) {
      return clean.slice(0, -3) + 'f' + punct;
    }
    if (lower.endsWith('ses') && lower.length > 4 && /[szx]es$/.test(lower)) {
      return clean.slice(0, -2) + punct;
    }
    if (lower.endsWith('s') && !lower.endsWith('ss') && lower.length > 3) {
      // Don't strip 's' from words that end in 'ss' (class, pass)
      // or from very short words (is, us, as)
      if (['is', 'us', 'as', 'has', 'was'].includes(lower)) return word;
      return clean.slice(0, -1) + punct;
    }

    // -tion → base verb (if not in explicit map)
    if (lower.endsWith('tion') && lower.length > 6) {
      const base = clean.slice(0, -4);
      // Common patterns: -ation → -ate, -ition → -ite, -ution → -ute
      if (base.endsWith('a')) return base + 'te' + punct;
      if (base.endsWith('i')) return base + 'te' + punct;
      if (base.endsWith('u')) return base + 'te' + punct;
      // Words where -tion attaches to a base ending in t (exhaust-ion, digest-ion)
      // The base already contains the t, just strip -ion
      if (base.endsWith('t') || base.endsWith('s')) return base + punct;
      return base + 'e' + punct;
    }

    // -ment → base verb
    if (lower.endsWith('ment') && lower.length > 6) {
      return clean.slice(0, -4) + punct;
    }

    // -ness → base adjective
    if (lower.endsWith('ness') && lower.length > 6) {
      return clean.slice(0, -4) + punct;
    }

    // -able/-ible → base verb
    if (lower.endsWith('able') && lower.length > 6) {
      return clean.slice(0, -4) + punct;
    }
    if (lower.endsWith('ible') && lower.length > 6) {
      return clean.slice(0, -4) + punct;
    }

    // -ing → base verb (gerund)
    if (lower.endsWith('ing') && lower.length > 5) {
      const base = clean.slice(0, -3);
      // Handle doubled consonants (running → run)
      if (base.length >= 3 && base[base.length - 1] === base[base.length - 2]) {
        return base.slice(0, -1) + punct;
      }
      return base + punct;
    }

    return word;
  });

  return lemmatized.join(' ');
}

// ---------------------------------------------------------------------------
// Rule 12: Arrow causality notation (ultra mode — JuliusBrussee style)
// ---------------------------------------------------------------------------

function arrowCausality(sentence) {
  // Replace common causality patterns with arrow notation
  let result = sentence
    // "X causes Y" → "X → Y"
    .replace(/\b(\w+(?:\s+\w+){0,3})\s+(causes?|leads to|results in|triggers)\s+(\w+(?:\s+\w+){0,3})\b/gi, '$1 → $3')
    // "X because Y" → "Y → X"
    .replace(/\b(\w+(?:\s+\w+){0,3})\s+because\s+(\w+(?:\s+\w+){0,3})\b/gi, '$2 → $1')
    // "if X then Y" → "X → Y"
    .replace(/\bif\s+(\w+(?:\s+\w+){0,3})\s+then\s+(\w+(?:\s+\w+){0,3})\b/gi, '$1 → $2')
    // "X therefore Y" → "X → Y"
    .replace(/\b(\w+(?:\s+\w+){0,3})\s+therefore\s+(\w+(?:\s+\w+){0,3})\b/gi, '$1 → $2')
    // "X so Y" → "X → Y"
    .replace(/\b(\w+(?:\s+\w+){0,3})\s+so\s+(\w+(?:\s+\w+){0,3})\b/gi, '$1 → $2');

  return result;
}

// ---------------------------------------------------------------------------
// Rule 8b: Ultra word limit (3 words, JuliusBrussee style)
// ---------------------------------------------------------------------------

function limitWordsUltra(sentence, maxWords = 4) {
  const words = sentence.split(/\s+/);
  if (words.length <= maxWords) return sentence;

  // Try to split on comma or arrow first
  const commaIdx = sentence.indexOf(',');
  const arrowIdx = sentence.indexOf('→');

  if (arrowIdx > 0 && arrowIdx < sentence.length / 2) {
    return sentence.slice(0, arrowIdx + 1).trim() + ' ' +
           sentence.slice(arrowIdx + 1).split(/\s+/).slice(0, 2).join(' ');
  }

  if (commaIdx > 0 && commaIdx < sentence.length / 2) {
    const firstPart = sentence.slice(0, commaIdx).trim();
    const firstWords = firstPart.split(/\s+/);
    if (firstWords.length <= maxWords) return firstPart;
  }

  return words.slice(0, maxWords).join(' ');
}

